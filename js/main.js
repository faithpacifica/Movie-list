let myWatchlist = watchlistItemsFromLocalStorage();

function addMovieToWatchlist(movieId) {
    if (!myWatchlist.includes(movieId)) {
        myWatchlist.push(movieId);
    }
}

if (moviesEl)
    moviesEl.addEventListener('click', function (event) {
        if (event.target.className == 'movie__btn') {
            let idOfCurrentMovie = +event.target.getAttribute('data-id');
            addMovieToWatchlist(idOfCurrentMovie);
            watchlistItemsToLocalStorage()
        }
    })


function watchlistItemsToLocalStorage() {
    localStorage.setItem('myWatchlist', JSON.stringify(myWatchlist))
}

function watchlistItemsFromLocalStorage() {
    let result = JSON.parse(localStorage.getItem('myWatchlist'));
    return result ? result : []; //* 
}

function createWatchlistItemDiv(el) {
    let divEl = document.createElement('div');
    divEl.className = 'movie';
    divEl.innerHTML = `
        <img src="img/${el.poster}" class="movie__poster">
                        <div class="movie__info">
                            <h2 class="movie__title">${el.title}</h2>
                            <h3 class="movie__genre">${el.genres}</h3>
                            <button class=" movie__btn delete-btn" type="button" data-id="${el.id}">Remove from watchlist</button>
                        </div>
        `;
    return divEl;
}

const watchlistItemsEl = document.getElementById('moviesBasket');

if(watchlistItemsEl){
    showWatchlistItems();
}

function showWatchlistItems(){
    watchlistItemsEl.innerHTML = '';
    let myWatchlistArr = watchlistItemsFromLocalStorage();
    
    myWatchlistArr.forEach((el)=>{
        let movie = movies.filter((item)=>{
        return item.id == el;
        });
        let element = createWatchlistItemDiv(movie[0]);
        watchlistItemsEl.appendChild(element);
    });
} 

// 8-ish DELETE

if(watchlistItemsEl){
    watchlistItemsEl.addEventListener('click',function(event){
      if(event.target.className.includes('delete-btn')){
         let elId = event.target.getAttribute('data-id');
          let myWatchlistArr = watchlistItemsFromLocalStorage();
          myWatchlistArr.splice(myWatchlistArr.indexOf(+elId),1);
          localStorage.setItem('myWatchlist',JSON.stringify(myWatchlistArr));
          let parentDiv = event.target.closest('.movie');
          removeItemFromWatchlist(parentDiv);
          showWatchlistItems();  
      };
    });
    };
    
    function removeItemFromWatchlist(divEl){
       watchlistItemsEl.removeChild(divEl)
    }