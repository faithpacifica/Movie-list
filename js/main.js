// 2-ish
function createMovieDiv(movieObject) {
    let divEl = document.createElement('div');
    divEl.className = 'movie';
    divEl.innerHTML = `
    <img src="img/${movieObject.poster}" class="movie__poster" alt = "${movieObject.title}">
    <div class="movie__info">
        <h2 class="movie__title">${movieObject.title}</h2>
        <h3 class="movie__genre">${movieObject.genres}</h3> 
        <button class="movie__btn" type="button" data-id="${movieObject.id}">Add to watchlist</button>
    </div> 
    `;
    return divEl;
}

// 3-ish
function appendMovies() {
    movies.forEach((el) => {
        let movieDiv = createMovieDiv(el);
        moviesEl.appendChild(movieDiv);
    })
}
if (moviesEl) appendMovies()


// 4-ish Kategoriya
function filterMoviesByCategory(category) {
    let result = movies.filter((el) => {
        return el.genres.includes(category);
    });
    return result;
}

//5-ish Kategoriyalangan filmlarni ekranga chiqarish
const categoryButtonsEl = document.getElementById('sidebar');

if (categoryButtonsEl) {
    categoryButtonsEl.addEventListener('click', function (event) {
        if (event.target.className.includes('genre') || event.target.className.includes('genre__item')) {
            let selectedCat = event.target.innerText;
            let selectedMovies;

            if (selectedCat !== "All Categories") {
                selectedMovies = filterMoviesByCategory(selectedCat);
            } else {
                selectedMovies = movies;
            }

            moviesEl.innerHTML = '';
            appendSelectedMovies(selectedMovies);
        }
    });
}

function appendSelectedMovies(smovies) {
    smovies.forEach((el) => {
        let movieDiv = createMovieDiv(el);
        moviesEl.appendChild(movieDiv);
    });
}

// 6- ish SEARCH

function searchMovie(searchText) {
    let resultArr = movies.filter((el) => {
        return el.title.toLowerCase().includes(searchText) || el.title.toUpperCase().includes(searchText);
    });
    return resultArr;
}

const searchInput = document.getElementById('search');

if(searchInput) {
    searchInput.addEventListener('keyup', function () {
    let searchedMovies = searchMovie(searchInput.value);
    moviesEl.innerHTML = '';
    appendSelectedMovies(searchedMovies);
});
}

